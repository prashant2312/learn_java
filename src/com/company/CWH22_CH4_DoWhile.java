package com.company;

public class CWH22_CH4_DoWhile {
    public static void main(String[] args) {
        int a=0;
        do{
            System.out.println(a);
            a++;
        }while(a==1);

        int n=1;
        do{
            System.out.println(n);
            n++;
        }while(n<=45);
    }
}
